public class BeanPersonal {
    private Integer codPer;
    private String nomPer;
    private String apePer;
    private String dirPer;
    private String estadoPer;
    private String sexo;
    private Integer dni;

    public Integer getCodPer() {
        return codPer;
    }

    public void setCodPer(Integer codPer) {
        this.codPer = codPer;
    }

    public String getNomPer() {
        return nomPer;
    }

    public void setNomPer(String nomPer) {
        this.nomPer = nomPer;
    }

    public String getApePer() {
        return apePer;
    }

    public void setApePer(String apePer) {
        this.apePer = apePer;
    }

    public String getDirPer() {
        return dirPer;
    }

    public void setDirPer(String dirPer) {
        this.dirPer = dirPer;
    }

    public String getEstadoPer() {
        return estadoPer;
    }

    public void setEstadoPer(String estadoPer) {
        this.estadoPer = estadoPer;
    }

    public String getSexo() {
        return sexo;
    }

    public void setSexo(String sexo) {
        this.sexo = sexo;
    }

    public Integer getDni() {
        return dni;
    }

    public void setDni(Integer dni) {
        this.dni = dni;
    }
    
    
}
