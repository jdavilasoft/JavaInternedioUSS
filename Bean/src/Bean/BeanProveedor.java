package Bean;

/**
 *
 * @author Luis Segura
 */
public class BeanProveedor {
    private Integer codProv;
    private String razonSocial;
    private String contacProv;
    private String telProv;
    private String dirProv;
    private String celProv;
    private String rucProv;

    public Integer getCodProv() {
        return codProv;
    }

    public void setCodProv(Integer codProv) {
        this.codProv = codProv;
    }

    public String getRazonSocial() {
        return razonSocial;
    }

    public void setRazonSocial(String razonSocial) {
        this.razonSocial = razonSocial;
    }

    public String getContacProv() {
        return contacProv;
    }

    public void setContacProv(String contacProv) {
        this.contacProv = contacProv;
    }

    public String getTelProv() {
        return telProv;
    }

    public void setTelProv(String telProv) {
        this.telProv = telProv;
    }

    public String getDirProv() {
        return dirProv;
    }

    public void setDirProv(String dirProv) {
        this.dirProv = dirProv;
    }

    public String getCelProv() {
        return celProv;
    }

    public void setCelProv(String celProv) {
        this.celProv = celProv;
    }

    public String getRucProv() {
        return rucProv;
    }

    public void setRucProv(String rucProv) {
        this.rucProv = rucProv;
    }
    
    
}
